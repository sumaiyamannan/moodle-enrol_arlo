<template>
  <ElementAdminForm
    :type="type"
    :error="error"
    :activity-state="activityState"
    @remove="$emit('remove')"
  >
    <template v-slot:content>
      <Uniform
        v-if="initialValues"
        v-slot="{ getSubmitting }"
        :initial-values="initialValues"
        class="tui-elementEditMultiChoiceMulti"
        validation-mode="submit"
        :validate="validator"
        input-width="full"
        vertical
        @submit="handleSubmit"
      >
        <FormRow
          :label="$str('question_title', 'performelement_multi_choice_multi')"
          required
        >
          <FormText
            name="rawTitle"
            :validations="v => [v.required(), v.maxLength(1024)]"
          />
        </FormRow>
        <FormRow
          :label="
            $str('multi_select_options', 'performelement_multi_choice_multi')
          "
          required
        >
          <FieldArray v-slot="{ items, push, remove }" path="answers">
            <Repeater
              :rows="items"
              :min-rows="minRows"
              :delete-icon="true"
              :allow-deleting-first-items="false"
              @add="push()"
              @remove="(item, i) => remove(i)"
            >
              <template v-slot="{ row, index }">
                <div class="tui-elementEditMultiChoiceMulti__option">
                  <FormText
                    :name="[index]"
                    :validations="v => [v.required()]"
                    :aria-label="
                      $str(
                        'answer_text',
                        'performelement_multi_choice_multi',
                        index + 1
                      )
                    "
                  />
                </div>
              </template>
              <template v-slot:add>
                <ButtonIcon
                  :aria-label="$str('add', 'core')"
                  :styleclass="{ small: true }"
                  class="tui-elementEditMultiChoiceMulti__addOption"
                  @click="push()"
                >
                  <AddIcon />
                </ButtonIcon>
              </template>
            </Repeater>
          </FieldArray>
        </FormRow>

        <FormRow
          :label="
            $str('response_restriction', 'performelement_multi_choice_multi')
          "
          :helpmsg="
            $str(
              'restriction_response_help_text',
              'performelement_multi_choice_multi'
            )
          "
        >
          <InputSet char-length="full">
            <FormNumber name="min" char-length="4" />
            <InputSizedText :id="$id('aria-describedby')">
              {{
                $str(
                  'restriction_minimum_label',
                  'performelement_multi_choice_multi'
                )
              }}
            </InputSizedText>
          </InputSet>
        </FormRow>

        <FormRow>
          <InputSet char-length="full">
            <FormNumber name="max" char-length="4" />
            <InputSizedText>
              {{
                $str(
                  'restriction_maximum_label',
                  'performelement_multi_choice_multi'
                )
              }}
            </InputSizedText>
          </InputSet>
        </FormRow>

        <IdentifierInput />

        <div class="tui-elementEditMultiChoiceMulti__required">
          <Checkbox v-model="responseRequired" name="responseRequired">
            {{ $str('section_element_response_required', 'mod_perform') }}
          </Checkbox>
        </div>

        <FormRow class="tui-elementEditMultiChoiceMulti__action-buttons">
          <FormActionButtons :submitting="getSubmitting()" @cancel="cancel" />
        </FormRow>
      </Uniform>
    </template>
  </ElementAdminForm>
</template>

<script>
import AddIcon from 'tui/components/icons/Add';
import AdminFormMixin from 'mod_perform/components/element/admin_form/AdminFormMixin';
import ButtonIcon from 'tui/components/buttons/ButtonIcon';
import Checkbox from 'tui/components/form/Checkbox';
import ElementAdminForm from 'mod_perform/components/element/ElementAdminForm';
import FormActionButtons from 'mod_perform/components/element/admin_form/ActionButtons';
import IdentifierInput from 'mod_perform/components/element/admin_form/IdentifierInput';
import Repeater from 'tui/components/form/Repeater';
import {
  Uniform,
  FormRow,
  FieldArray,
  FormNumber,
  FormText,
} from 'tui/components/uniform';
import InputSet from 'tui/components/form/InputSet';
import InputSizedText from 'tui/components/form/InputSizedText';

const MIN_OPTIONS = 2;
const OPTION_PREFIX = 'option_';

export default {
  components: {
    AddIcon,
    ButtonIcon,
    Checkbox,
    ElementAdminForm,
    FieldArray,
    FormActionButtons,
    FormRow,
    FormNumber,
    FormText,
    IdentifierInput,
    Repeater,
    Uniform,
    InputSet,
    InputSizedText,
  },
  mixins: [AdminFormMixin],
  props: {
    type: Object,
    title: String,
    rawTitle: String,
    identifier: String,
    isRequired: {
      type: Boolean,
      default: false,
    },
    activityState: {
      type: Object,
      required: true,
    },
    data: Object,
    rawData: Object,
    error: String,
    min: Number,
    max: Number,
  },
  data() {
    const initialValues = {
      title: this.title,
      rawTitle: this.rawTitle,
      identifier: this.identifier,
      responseRequired: this.isRequired,
      answers: [],
      min: '',
      max: '',
    };

    if (Object.keys(this.rawData).length == 0) {
      initialValues.answers = ['', ''];
    } else {
      this.rawData.options.forEach(item => {
        initialValues.answers.push(item.value);
      });
      if (typeof this.rawData.settings !== 'undefined') {
        if (Object.keys(this.rawData.settings).length > 0) {
          this.rawData.settings.forEach(item => {
            if (item.name == 'min') {
              initialValues.min = item.value;
            } else if (item.name == 'max') {
              initialValues.max = item.value;
            }
          });
        }
      }
    }

    return {
      initialValues: initialValues,
      minRows: MIN_OPTIONS,
      responseRequired: this.isRequired,
    };
  },
  methods: {
    /**
     * Handle multi choice element submit data
     * @param values
     */
    handleSubmit(values) {
      const optionList = [];
      const restrictionVal = [];

      values.answers.forEach((item, index) => {
        optionList.push({ name: OPTION_PREFIX + index, value: item });
      });

      restrictionVal.push({ name: 'min', value: values.min });
      restrictionVal.push({ name: 'max', value: values.max });

      this.$emit('update', {
        title: values.rawTitle,
        identifier: values.identifier,
        data: {
          options: optionList,
          settings: restrictionVal,
        },
        is_required: this.responseRequired,
      });
    },

    /**
     * Cancel edit form
     */
    cancel() {
      this.$emit('display');
    },

    validator(values) {
      const errors = {};
      if (values.min !== '' && Number(values.min) <= 0) {
        errors.min = this.$str(
          'minmaximum_zero_error',
          'performelement_multi_choice_multi'
        );
      }
      if (values.max !== '' && Number(values.max) <= 0) {
        errors.max = this.$str(
          'minmaximum_zero_error',
          'performelement_multi_choice_multi'
        );
      }
      if (values.min !== '' && values.answers.length < Number(values.min)) {
        errors.min = this.$str(
          'minmaximum_value_error',
          'performelement_multi_choice_multi'
        );
      }
      if (values.max !== '' && values.answers.length < Number(values.max)) {
        errors.max = this.$str(
          'minmaximum_value_error',
          'performelement_multi_choice_multi'
        );
      }
      if (
        values.min !== '' &&
        values.max !== '' &&
        Number(values.min) > Number(values.max)
      ) {
        errors.max = this.$str(
          'minmaximum_less_error',
          'performelement_multi_choice_multi'
        );
      }
      return errors;
    },
  },
};
</script>
